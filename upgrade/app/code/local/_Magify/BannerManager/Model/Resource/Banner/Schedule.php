class Magify_BannerManager_Model_Resource_Banner_Schedule extends Mage_Core_Model_Resource_Db_Abstract
{
    /**
     * Define main table
     *
     */
    protected function _construct()
    {
        $this->_init('magify_bannermanager/banner_schedule', 'banner_id');
    }
    
    public function load(Mage_Core_Model_Abstract $object, $value)
    {

        $read = $this->_getReadAdapter();
        if ($read && !is_null($value)) {
            $select = $this->_getLoadSelect('banner_id', $value, $object);
            $data = $read->fetchAll($select);

            if ($data) {
                $object->setData($data);
            }
        }

        $this->unserializeFields($object);
        $this->_afterLoad($object);

        return $this;
    }
}